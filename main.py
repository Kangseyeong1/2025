        t = tokens[i]
        if t == '(':
            stack.append(defaultdict(int))
            i += 1
        elif t == ')':
            i += 1
            mult = 1
            if i < len(tokens) and tokens[i].isdigit():
                mult = int(tokens[i]); i += 1
            group = stack.pop()
