# 3. 사용자 입력 받기: MBTI 선택
mbti_types = ["--MBTI를 선택해주세요--"] + sorted(list(mbti_careers.keys())) # 드롭다운 메뉴에 나올 MBTI 목록
selected_mbti = st.selectbox("당신의 MBTI 유형은 무엇인가요?", mbti_types)

# 4. 선택된 MBTI에 따라 직업 추천 보여주기
if selected_mbti != "--MBTI를 선택해주세요--":
    st.header(f"✨ {selected_mbti} 유형을 위한 추천 직업 ✨")
    careers = mbti_careers.get(selected_mbti, []) # 선택된 MBTI에 맞는 직업 리스트 가져오기

    if careers: # 직업 리스트가 비어있지 않다면
        for i, career in enumerate(careers):
            st.write(f"- {career}") # 목록 형태로 보여주기
        st.write("---")
        st.info("이 추천은 일반적인 경향을 바탕으로 합니다. 당신의 개인적인 흥미와 재능이 가장 중요해요!")
    else: # 만약 MBTI 정보가 없으면 (거의 없을 테지만)
        st.warning("아직 이 MBTI 유형에 대한 직업 정보가 준비되지 않았어요. 죄송합니다! ㅠㅠ")
